
vim.cmd("set expandtab")        -- Usa espaços ao invés de tabulações
vim.cmd("set tabstop=4")        -- Define o tamanho da tabulação como 4 espaços
vim.cmd("set softtabstop=4")    -- Define o número de espaços ao pressionar Tab para ser 4
vim.cmd("set shiftwidth=4")     -- Define o número de espaços ao usar comandos de indentação, como >> e <<
vim.cmd("set smarttab")         -- Ajusta a indentação de modo inteligente
vim.cmd("set autoindent")       -- Habilita indentação automática
vim.cmd("set smartindent")
vim.opt.relativenumber = true
